

public class CarInfo {
	String carName = "Honda";
	double carNumber = 0;

	// with return type
	String getName() {
		return carName;
	}

	// update name value
	void setName(String name) {
		carName = name;
	}

	// with return type
	double getNumber() {
		return carNumber;
	}

//update number value
	void setNumber(double num) {
		carNumber = num;
	}
}
